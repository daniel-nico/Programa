namespace ArrayEjercicio01;

class Ejercicio03
{
    static void Main(string[] args)
    {
        char [] inicial = new char[3];

        inicial[0] = 'J';
        inicial[1] = 'M';
        inicial[2] = 's';

        Console.WriteLine("Iniciales almacenados en el arreglo:");
        Console.WriteLine("1. " + inicial[0]);
        Console.WriteLine("2. " + inicial[1]);
        Console.WriteLine("3. " + inicial[2]);
    }
}